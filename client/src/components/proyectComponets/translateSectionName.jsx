export function TranslateSectionName(sectionKey) {
    switch (sectionKey) {
      case "livingRoom":
        return "Salón ";
      case "kitchen":
        return "Cocina";
      case "hall":
        return "Pasillo";
      case "room":
        return "Habitación";
      case "bathRoom":
        return "Baño";
      case " terrace":
        return "Terraza";
      case "laundry":
        return "Lavandería ";
      case "pool":
        return "Piscina";
      case "roof":
        return "Techo";
    }
  }
  
  